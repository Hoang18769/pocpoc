import { create } from 'zustand';
import { devtools } from 'zustand/middleware';
import api from '@/utils/axios'; 

// Event constants
export const STORE_EVENTS = {
  CHAT_LIST_LOAD: 'chat_list_load',
  CHAT_CREATED: 'chat_created',
  MESSAGE_RECEIVED: 'message_received',
  NOTIFICATION_RECEIVED: 'notification_received',
  NOTIFICATIONS_LOAD: 'notifications_load',
  NEWSFEED_LOAD: 'newsfeed_load',
  POST_CREATED: 'post_created',
  SEARCH_PERFORMED: 'search_performed',
};

const useAppStore = create(
  devtools((set, get) => ({
    // ============ CHAT STATE ============
    chatList: [],
    conversationMap: new Map(),
    isLoadingChats: false,
    error: null,

    fetchChatList: async () => {
      set({ isLoadingChats: true, error: null });
      try {
        const res = await api.get('/v1/chat');
        const data = res.data.body;
        const conversationMap = new Map();
        const currentUserId = getCurrentUserId();

        data.forEach(chat => {
          const otherUser = chat.participants?.find(p => p.id !== currentUserId);
          if (otherUser) conversationMap.set(otherUser.id, chat.id);
        });

        // ‚úÖ Reverse the chat list when fetching
        const reversedData = [...data].reverse();

        set({ 
          chatList: reversedData, 
          conversationMap,
          isLoadingChats: false,
          error: null
        });

        console.log(`üìä ${STORE_EVENTS.CHAT_LIST_LOAD}`,reversedData);
      } catch (error) {
        console.error('‚ùå Error fetching chats:', error);
        set({ 
          isLoadingChats: false, 
          error: 'Failed to load chats' 
        });
      }
    },
// Trong store
updateChatListAfterMessage: (chatId, lastMessage) => {
  set((state) => ({
    chatList: state.chatList.map(chat => 
      chat.id === chatId 
        ? { ...chat, lastMessage, updatedAt: new Date().toISOString() }
        : chat
    ).sort((a, b) => new Date(b.updatedAt) - new Date(a.updatedAt))
  }));
},
    getUserByChatId: (chatId) => {
      const chat = get().chatList.find(c => c.id === chatId);
      return chat ? chat.user : null;
    },

    markChatAsRead: async (chatId) => {
      set(state => ({
        chatList: state.chatList.map(chat => 
          (chat.chatId === chatId || chat.id === chatId)
            ? { ...chat, notReadMessageCount: 0 }
            : chat
        )
      }));   
    },

    onMessageReceived: (message, isCurrentChatOpen = false) => {
      set(state => {
        const updatedChats = state.chatList
          .map(chat => {
            if (chat.chatId === message.chatId || chat.id === message.chatId) {
              return { 
                ...chat, 
                lastMessage: message, 
                updatedAt: message.createdAt,
                notReadMessageCount: isCurrentChatOpen 
                  ? 0 
                  : (chat.notReadMessageCount || 0) + 1
              };
            }
            return chat;
          })
          .sort((a, b) => new Date(a.updatedAt) - new Date(b.updatedAt)); // ‚úÖ Sort ascending (oldest first)

        return { chatList: updatedChats };
      });

      console.log(`üìä ${STORE_EVENTS.MESSAGE_RECEIVED} - ${message.chatId} (maintaining reverse order)`);
    },

    onChatCreated: (newChat) => {
      const currentUserId = getCurrentUserId();
      const otherUser = newChat.participants?.find(p => p.id !== currentUserId);

      set(state => ({
        // ‚úÖ Add new chat at the end (to maintain reverse chronological order)
        chatList: [...state.chatList, newChat],
        conversationMap: otherUser 
          ? new Map(state.conversationMap).set(otherUser.id, newChat.id)
          : state.conversationMap
      }));

      console.log(`üìä ${STORE_EVENTS.CHAT_CREATED} - ${newChat.id} (added to end)`);
    },

    // ============ NOTIFICATIONS STATE ============
    notifications: [],
    isLoadingNotifications: false,
    unreadNotificationCount: 0,

    fetchNotifications: async (force = false, page = 0, size = 10) => {
      const { notifications, isLoadingNotifications } = get();
      
      // N·∫øu ƒë√£ c√≥ th√¥ng b√°o v√† kh√¥ng b·∫Øt bu·ªôc fetch l·∫°i, th√¨ skip
      if (!force && notifications.length > 0) {
        return;
      }
      
      // N·∫øu ƒëang loading, kh√¥ng fetch l·∫°i
      if (isLoadingNotifications) {
        return;
      }

      set({ isLoadingNotifications: true, error: null });
      try {
        const res = await api.get('/v1/notifications', {
          params: { page, size }
        });
        
        console.log('üìä Notifications API response:', res);
        
        // X·ª≠ l√Ω response data gi·ªëng nh∆∞ hook
        const responseData = res.data;
        let data = [];
        
        if (responseData) {
          // Ki·ªÉm tra c√°c c·∫•u tr√∫c response kh√°c nhau
         if (responseData.body && Array.isArray(responseData.body)) {
            data = responseData.body; // Body response
          } else if (Array.isArray(responseData)) {
            data = responseData; // Direct array
          }
        }
        
        // ‚úÖ FIX: Ch·ªâ t√≠nh count khi kh√¥ng c√≥ notifications t·ª´ socket
        const currentNotifications = get().notifications;
        let finalNotifications = data;
        let unreadCount = 0;
        
        if (currentNotifications.length > 0) {
          // ‚úÖ Merge notifications t·ª´ API v·ªõi notifications t·ª´ socket
          // Tr√°nh duplicate b·∫±ng c√°ch filter theo ID
          const apiNotificationIds = new Set(data.map(n => n.id));
          const socketOnlyNotifications = currentNotifications.filter(n => !apiNotificationIds.has(n.id));
          
          finalNotifications = [...socketOnlyNotifications, ...data];
          unreadCount = finalNotifications.filter(n => !n.isRead).length;
        } else {
          // ‚úÖ Ch·ªâ c√≥ notifications t·ª´ API
          unreadCount = data.filter(n => !n.isRead).length;
        }
        
        set({ 
          notifications: finalNotifications,
          unreadNotificationCount: unreadCount,
          isLoadingNotifications: false,
          error: null
        });

        console.log(`üìä ${STORE_EVENTS.NOTIFICATIONS_LOAD} - ${finalNotifications.length} notifications, ${unreadCount} unread`);
        return finalNotifications;
      } catch (error) {
        console.error('‚ùå Error fetching notifications:', error);
        set({ 
          isLoadingNotifications: false,
          error: 'Failed to load notifications' 
        });
        throw error;
      }
    },

    onNotificationReceived: (notification) => {
      const { notifications } = get();
      
      // ‚úÖ FIX: N·∫øu danh s√°ch r·ªóng, fetch song song nh∆∞ng v·∫´n x·ª≠ l√Ω notification t·ª´ socket
      if (notifications.length === 0) {
        console.log('üìä Empty notifications list, fetching from API...');
        // Fetch song song, kh√¥ng return
        get().fetchNotifications(true);
        // Ti·∫øp t·ª•c x·ª≠ l√Ω notification t·ª´ socket
      }

      // ‚úÖ Ki·ªÉm tra xem th√¥ng b√°o ƒë√£ t·ªìn t·∫°i ch∆∞a (tr√°nh duplicate)
      const existingNotification = notifications.find(n => n.id === notification.id);
      if (existingNotification) {
        console.log(`üìä Notification ${notification.id} already exists, skipping...`);
        return;
      }

      // ‚úÖ Ch·ªâ tƒÉng count n·∫øu notification ch∆∞a ƒë·ªçc
      set(state => ({
        notifications: [notification, ...state.notifications],
        unreadNotificationCount: !notification.isRead 
          ? state.unreadNotificationCount + 1
          : state.unreadNotificationCount
      }));

      console.log(`üìä ${STORE_EVENTS.NOTIFICATION_RECEIVED} - ${notification.id || 'new notification'}`);
    },

    markNotificationAsRead: async (notificationId) => {
      try {
        // ‚úÖ Ki·ªÉm tra notification c√≥ t·ªìn t·∫°i v√† ch∆∞a ƒë·ªçc kh√¥ng
        const { notifications } = get();
        const notification = notifications.find(n => n.id === notificationId);
        
        if (!notification || notification.isRead) {
          return; // Kh√¥ng c·∫ßn update count
        }
        
        set(state => ({
          notifications: state.notifications.map(n =>
            n.id === notificationId
              ? { ...n, isRead: true }
              : n
          ),
          unreadNotificationCount: Math.max(0, state.unreadNotificationCount - 1)
        }));
      } catch (error) {
        console.error('‚ùå Error marking notification as read:', error);
      }
    },

    markAllNotificationsAsRead: async () => {
      try {
        // G·ªçi API ƒë·ªÉ ƒë√°nh d·∫•u t·∫•t c·∫£ ƒë√£ ƒë·ªçc tr√™n server (n·∫øu c·∫ßn)
        // await api.patch('/v1/notifications/read-all');
        
        set(state => ({
          notifications: state.notifications.map(notification =>
            ({ ...notification, isRead: true })
          ),
          unreadNotificationCount: 0 // ‚úÖ Reset v·ªÅ 0
        }));
      } catch (error) {
        console.error('‚ùå Error marking all notifications as read:', error);
      }
    },

    removeNotification: (notificationId) => {
      set(state => {
        const notification = state.notifications.find(n => n.id === notificationId);
        const wasUnread = notification && !notification.isRead;
        
        return {
          notifications: state.notifications.filter(n => n.id !== notificationId),
          unreadNotificationCount: wasUnread 
            ? Math.max(0, state.unreadNotificationCount - 1)
            : state.unreadNotificationCount
        };
      });
    },

    clearAllNotifications: async () => {
      try {
        // G·ªçi API ƒë·ªÉ x√≥a t·∫•t c·∫£ th√¥ng b√°o tr√™n server (n·∫øu c·∫ßn)
        // await api.delete('/v1/notifications');
        
        set({ 
          notifications: [],
          unreadNotificationCount: 0
        });
      } catch (error) {
        console.error('‚ùå Error clearing all notifications:', error);
      }
    },

    // ‚úÖ Th√™m method ƒë·ªÉ sync count t·ª´ notifications array
    syncUnreadCount: () => {
      const { notifications } = get();
      const unreadCount = notifications.filter(n => !n.isRead).length;
      
      set({ unreadNotificationCount: unreadCount });
      console.log(`üìä Synced unread count: ${unreadCount}`);
    },

    // ============ CHAT NAVIGATION & SELECTION LOGIC ============
    selectedChatId: null,
    virtualChatUser: null,

    navigateToChat: (userId, userInfo = null) => {
      const { chatList } = get();

      const existingChat = chatList.find(chat => 
        chat.target && chat.target.username === userInfo?.username
      );

      if (existingChat) {
        console.log(`üöÄ Navigating to existing chat: ${existingChat.chatId} with user: ${userId}`);
        return {
          type: 'existing',
          chatId: existingChat.chatId,
          userId: userId
        };
      } else {
        console.log(`üöÄ Creating virtual chat with user: ${userId}`);
        return {
          type: 'virtual',
          chatId: null,
          userId: userId,
          userInfo: userInfo
        };
      }
    },

    selectChat: (chatId) => {
      set({ 
        selectedChatId: chatId,
        virtualChatUser: null
      });
      console.log(`‚úÖ Selected chat: ${chatId}`);
    },

    showVirtualChat: (userId, userInfo) => {
      set({ 
        selectedChatId: null,
        virtualChatUser: {
          id: userId,
          ...userInfo
        }
      });
      console.log(`‚úÖ Showing virtual chat with user: ${userId}`);
    },

    clearChatSelection: () => {
      set({ 
        selectedChatId: null,
        virtualChatUser: null
      });
    },

    createNewChat: async (userId, firstMessage) => {
      try {
        const res = await api.post('/v1/chat', {
          participantId: userId,
          message: firstMessage
        });

        if (res.data.code === 200) {
          const newChat = res.data.body;
          get().onChatCreated(newChat);

          set({ 
            selectedChatId: newChat.id,
            virtualChatUser: null
          });

          console.log(`‚úÖ New chat created and selected: ${newChat.id}`);
          return newChat;
        }
      } catch (error) {
        console.error('‚ùå Error creating new chat:', error);
        throw error;
      }
    },

    // ============ INITIALIZATION ============
    initializeApp: async () => {
      console.log('üöÄ Initializing app...');
      try {
        // Fetch both chats and notifications in parallel
        await Promise.all([
          get().fetchChatList(),
          get().fetchNotifications()
        ]);
        console.log('‚úÖ App initialized successfully');
      } catch (error) {
        console.error('‚ùå Error initializing app:', error);
        set({ error: 'Failed to initialize app' });
      }
    },

    // ============ UTILITY ============
    clearAllData: () => {
  set({
    chatList: [],
    conversationMap: new Map(),
    selectedChatId: null,
    virtualChatUser: null,
    notifications: [],
    unreadNotificationCount: 0,
    error: null,
    isLoadingChats: false,
    isLoadingNotifications: false,
  }, true); // üëà Th√™m `true` ƒë·ªÉ notify devtools n·∫øu ƒëang d√πng devtools middleware
},


    getChatByUserId: (userId) => get().conversationMap.get(userId),
    getSelectedChat: () => {
      const { selectedChatId, chatList } = get();
      return chatList.find(chat => chat.id === selectedChatId) || null;
    },

    // ============ AUTO FETCH NOTIFICATIONS ============
    ensureNotificationsLoaded: () => {
      const { notifications, isLoadingNotifications } = get();
      
      // N·∫øu danh s√°ch r·ªóng v√† kh√¥ng ƒëang loading, t·ª± ƒë·ªông fetch
      if (notifications.length === 0 && !isLoadingNotifications) {
        console.log('üìä Auto-fetching notifications (empty list)...');
        get().fetchNotifications(true);
      }
    },

  }), {
    name: 'app-store'
  })
);

function getCurrentUserId() {
  return localStorage.getItem('userId') || null;
}

export default useAppStore;